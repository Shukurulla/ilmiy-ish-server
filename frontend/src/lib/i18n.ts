export function t(key: string, lang: Lang = "uz"): string {
  return translations[lang]?.[key] || key;
}

export function getLocalizedField(obj: any, lang: Lang = "uz"): string {
  if (!obj) return "";
  if (typeof obj === "string") return obj;
  return obj[lang] || obj.uz || obj.ru || obj.en || "";
}
